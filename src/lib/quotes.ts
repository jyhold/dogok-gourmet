/** 명언 풀에서 균등 랜덤 1개. rng 주입으로 테스트 결정적. */
export function pickQuote(rng: () => number = Math.random): FoodQuote {
  const i = Math.floor(rng() * FOOD_QUOTES.length);
  // rng()가 정확히 1을 반환하는 경계는 마지막 항목으로 고정.
  return FOOD_QUOTES[Math.min(i, FOOD_QUOTES.length - 1)];
}
